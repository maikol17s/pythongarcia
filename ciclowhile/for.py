for i in range(11):
    
    if i%3==0: # con un argumento verifica si es multiplo de 3
        print(f'{i} es multiplo de 3')
    else:
        print(i)

for j in range(1,11):  #con 2 argumentos indica el inicio y  el final
    print(j)

for k in range(0,11,2): #con 3 argumentos indica el inicio, el final y el incremnto
    print(k)

for i in range(20,0,-2): #con 3 argumentos indica el inicio, el final y el decremento
    print(i)